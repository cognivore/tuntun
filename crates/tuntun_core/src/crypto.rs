//! Public-key and signature value types. The actual crypto computations live
//! in `tuntun_auth` — this module just defines the data types crossing port
//! boundaries.

use std::fmt;

use serde::{Deserialize, Serialize};
use subtle::ConstantTimeEq;

use crate::error::Error;

/// Ed25519 public key, 32 bytes.
#[derive(Clone, Copy, Serialize, Deserialize)]
pub struct Ed25519PublicKey(pub [u8; 32]);

impl Ed25519PublicKey {
    pub fn from_bytes(bytes: [u8; 32]) -> Self {
        Self(bytes)
    }

    /// Parse from a textual `ed25519:<base64>` representation.
    pub fn from_authorized_keys_line(line: &str) -> Result<Self, Error> {
        let trimmed = line.trim();
        let body = trimmed
            .strip_prefix("ed25519:")
            .ok_or_else(|| Error::crypto("authorized key missing ed25519: prefix"))?;
        let bytes = base64_decode(body)
            .map_err(|e| Error::crypto(format!("authorized key not base64: {e}")))?;
        if bytes.len() != 32 {
            return Err(Error::crypto(format!(
                "authorized key wrong length: {} (expected 32)",
                bytes.len()
            )));
        }
        let mut out = [0u8; 32];
        out.copy_from_slice(&bytes);
        Ok(Self(out))
    }

    pub fn to_authorized_keys_line(self) -> String {
        format!("ed25519:{}", base64_encode(&self.0))
    }
}

impl fmt::Debug for Ed25519PublicKey {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "Ed25519PublicKey({})", hex::encode(self.0))
    }
}

impl PartialEq for Ed25519PublicKey {
    fn eq(&self, other: &Self) -> bool {
        self.0.ct_eq(&other.0).into()
    }
}

impl Eq for Ed25519PublicKey {}

impl std::hash::Hash for Ed25519PublicKey {
    fn hash<H: std::hash::Hasher>(&self, state: &mut H) {
        self.0.hash(state);
    }
}

/// Ed25519 signature, 64 bytes.
#[derive(Clone, Copy, Serialize, Deserialize)]
pub struct Ed25519Signature(#[serde(with = "serde_arrays_64")] pub [u8; 64]);

impl fmt::Debug for Ed25519Signature {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "Ed25519Signature({}...)", hex::encode(&self.0[..8]))
    }
}

impl PartialEq for Ed25519Signature {
    fn eq(&self, other: &Self) -> bool {
        self.0.ct_eq(&other.0).into()
    }
}

impl Eq for Ed25519Signature {}

/// SHA-256 fingerprint, 32 bytes.
#[derive(Clone, Copy, Serialize, Deserialize)]
pub struct Fingerprint(pub [u8; 32]);

impl Fingerprint {
    pub fn from_hex(s: &str) -> Result<Self, Error> {
        let s = s.strip_prefix("sha256:").unwrap_or(s);
        let bytes = hex::decode(s).map_err(|e| Error::crypto(format!("hex: {e}")))?;
        if bytes.len() != 32 {
            return Err(Error::crypto(format!(
                "fingerprint wrong length: {} (expected 32)",
                bytes.len()
            )));
        }
        let mut out = [0u8; 32];
        out.copy_from_slice(&bytes);
        Ok(Self(out))
    }

    pub fn to_hex_with_prefix(self) -> String {
        format!("sha256:{}", hex::encode(self.0))
    }
}

impl fmt::Debug for Fingerprint {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "Fingerprint({})", self.to_hex_with_prefix())
    }
}

impl PartialEq for Fingerprint {
    fn eq(&self, other: &Self) -> bool {
        self.0.ct_eq(&other.0).into()
    }
}

impl Eq for Fingerprint {}

