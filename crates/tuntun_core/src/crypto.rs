mod serde_arrays_64 {
    use serde::{de::Error as _, Deserialize, Deserializer, Serializer};

    pub fn serialize<S: Serializer>(arr: &[u8; 64], s: S) -> Result<S::Ok, S::Error> {
        s.serialize_bytes(arr)
    }

    pub fn deserialize<'de, D: Deserializer<'de>>(d: D) -> Result<[u8; 64], D::Error> {
        let v = Vec::<u8>::deserialize(d)?;
        if v.len() != 64 {
            return Err(D::Error::custom(format!(
                "expected 64 bytes, got {}",
                v.len()
            )));
        }
        let mut out = [0u8; 64];
        out.copy_from_slice(&v);
        Ok(out)
    }
}

fn base64_encode(bytes: &[u8]) -> String {
    use base64::engine::general_purpose::STANDARD_NO_PAD;
    use base64::Engine as _;
    STANDARD_NO_PAD.encode(bytes)
}

fn base64_decode(s: &str) -> Result<Vec<u8>, base64::DecodeError> {
    use base64::engine::general_purpose::STANDARD_NO_PAD;
    use base64::Engine as _;
    STANDARD_NO_PAD.decode(s)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn pubkey_authorized_keys_round_trip() {
        let key = Ed25519PublicKey::from_bytes([7u8; 32]);
        let line = key.to_authorized_keys_line();
        assert!(line.starts_with("ed25519:"));
        let back = Ed25519PublicKey::from_authorized_keys_line(&line).unwrap();
        assert_eq!(key, back);
    }

    #[test]
    fn fingerprint_round_trip() {
        let fp = Fingerprint([42u8; 32]);
        let s = fp.to_hex_with_prefix();
        assert!(s.starts_with("sha256:"));
        let back = Fingerprint::from_hex(&s).unwrap();
        assert_eq!(fp, back);
    }

    #[test]
    fn fingerprint_rejects_wrong_length() {
        assert!(Fingerprint::from_hex("ab").is_err());
    }
}
